        int option = JOptionPane.showOptionDialog(this, "Are you sure you want to logout?",
                "Logout Confirmation",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]);
        
        if (option == 0) {
            this.dispose();
            new Login().setVisible(true);
        }
